package Exemplos.Escola;
public class Cracha {
    String dataInicial;
    String numero;
    String foto;
    String instituicao;
    Aluno aluno;

    public Cracha(
        String dataInicial,
        String numero,
        String foto,
        String instituicao,
        Aluno aluno
    ){
        this.dataInicial = dataInicial; 
        this.numero = numero;
        this.foto = foto;
        this.instituicao = instituicao;
        this.aluno = aluno;
    }

}
